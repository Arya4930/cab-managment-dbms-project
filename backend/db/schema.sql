-- Run this script in Oracle SQL*Plus / SQL Developer.
-- It recreates schema objects for the Cab Management System.

BEGIN EXECUTE IMMEDIATE 'DROP TABLE refunds CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE payment CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE ratings_reviews CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE ride_tracking CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE earnings CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE bookings CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE cab_maintenance CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE saved_location CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE feedback CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE drivers CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE cabs CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE users CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE bookings_seq'; EXCEPTION WHEN OTHERS THEN NULL; END;
/

CREATE TABLE users (
  user_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR2(120) NOT NULL,
  email VARCHAR2(150) NOT NULL,
  phone_number VARCHAR2(30) NOT NULL,
  user_type VARCHAR2(30) DEFAULT 'Passenger' NOT NULL,
  joined_date DATE DEFAULT SYSDATE NOT NULL,
  total_rides NUMBER DEFAULT 0 NOT NULL,
  CONSTRAINT uq_users_email UNIQUE (email)
);

CREATE TABLE cabs (
  cab_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type VARCHAR2(60) NOT NULL,
  model VARCHAR2(120) NOT NULL,
  license_plate VARCHAR2(30) NOT NULL,
  cab_status VARCHAR2(40) DEFAULT 'Active' NOT NULL,
  manufacture_year NUMBER(4),
  color VARCHAR2(60),
  CONSTRAINT uq_cabs_license_plate UNIQUE (license_plate)
);

CREATE TABLE drivers (
  driver_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_name VARCHAR2(120) NOT NULL,
  availability VARCHAR2(40) DEFAULT 'Available' NOT NULL,
  ratings NUMBER(3,2) DEFAULT 0,
  user_id NUMBER,
  cab_id NUMBER,
  license_no VARCHAR2(50),
  phone_number VARCHAR2(30),
  total_trips NUMBER DEFAULT 0 NOT NULL,
  joined_date DATE DEFAULT SYSDATE NOT NULL,
  CONSTRAINT fk_drivers_user FOREIGN KEY (user_id) REFERENCES users (user_id),
  CONSTRAINT fk_drivers_cab FOREIGN KEY (cab_id) REFERENCES cabs (cab_id)
);

CREATE TABLE bookings (
  booking_id VARCHAR2(20) PRIMARY KEY,
  pickup_loc VARCHAR2(250) NOT NULL,
  dropoff_loc VARCHAR2(250) NOT NULL,
  pickup_time DATE NOT NULL,
  status VARCHAR2(40) DEFAULT 'Scheduled' NOT NULL,
  fare NUMBER(10,2) DEFAULT 0 NOT NULL,
  user_id NUMBER NOT NULL,
  driver_id NUMBER NOT NULL,
  cab_id NUMBER NOT NULL,
  distance_km NUMBER(8,2) DEFAULT 0,
  CONSTRAINT fk_bookings_user FOREIGN KEY (user_id) REFERENCES users (user_id),
  CONSTRAINT fk_bookings_driver FOREIGN KEY (driver_id) REFERENCES drivers (driver_id),
  CONSTRAINT fk_bookings_cab FOREIGN KEY (cab_id) REFERENCES cabs (cab_id)
);

CREATE TABLE earnings (
  earning_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  earning_date DATE DEFAULT SYSDATE NOT NULL,
  driver_amount NUMBER(10,2) NOT NULL,
  booking_id VARCHAR2(20) NOT NULL,
  driver_id NUMBER NOT NULL,
  platform_fee NUMBER(10,2) DEFAULT 0 NOT NULL,
  trips NUMBER DEFAULT 1 NOT NULL,
  CONSTRAINT fk_earnings_booking FOREIGN KEY (booking_id) REFERENCES bookings (booking_id),
  CONSTRAINT fk_earnings_driver FOREIGN KEY (driver_id) REFERENCES drivers (driver_id)
);

CREATE TABLE ratings_reviews (
  review_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  rating NUMBER(3,2) NOT NULL,
  review CLOB,
  user_id NUMBER NOT NULL,
  driver_id NUMBER NOT NULL,
  booking_id VARCHAR2(20) NOT NULL,
  review_date DATE DEFAULT SYSDATE NOT NULL,
  CONSTRAINT fk_rr_user FOREIGN KEY (user_id) REFERENCES users (user_id),
  CONSTRAINT fk_rr_driver FOREIGN KEY (driver_id) REFERENCES drivers (driver_id),
  CONSTRAINT fk_rr_booking FOREIGN KEY (booking_id) REFERENCES bookings (booking_id)
);

CREATE TABLE payment (
  payment_id VARCHAR2(20) PRIMARY KEY,
  amount NUMBER(10,2) NOT NULL,
  payment_method VARCHAR2(40) NOT NULL,
  payment_status VARCHAR2(40) DEFAULT 'Pending' NOT NULL,
  booking_id VARCHAR2(20) NOT NULL,
  created_at DATE DEFAULT SYSDATE NOT NULL,
  CONSTRAINT fk_payment_booking FOREIGN KEY (booking_id) REFERENCES bookings (booking_id)
);

CREATE TABLE ride_tracking (
  tracking_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_location VARCHAR2(200) NOT NULL,
  time_stamp DATE DEFAULT SYSDATE NOT NULL,
  booking_id VARCHAR2(20) NOT NULL,
  speed_kmh NUMBER(6,2),
  track_status VARCHAR2(40) DEFAULT 'En Route' NOT NULL,
  CONSTRAINT fk_tracking_booking FOREIGN KEY (booking_id) REFERENCES bookings (booking_id)
);

CREATE TABLE refunds (
  refund_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reason VARCHAR2(200),
  amount NUMBER(10,2) NOT NULL,
  user_id NUMBER NOT NULL,
  cab_id NUMBER NOT NULL,
  booking_id VARCHAR2(20) NOT NULL,
  CONSTRAINT fk_refund_user FOREIGN KEY (user_id) REFERENCES users (user_id),
  CONSTRAINT fk_refund_cab FOREIGN KEY (cab_id) REFERENCES cabs (cab_id),
  CONSTRAINT fk_refund_booking FOREIGN KEY (booking_id) REFERENCES bookings (booking_id)
);

CREATE TABLE feedback (
  feedback_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  message CLOB,
  user_id NUMBER NOT NULL,
  CONSTRAINT fk_feedback_user FOREIGN KEY (user_id) REFERENCES users (user_id)
);

CREATE TABLE saved_location (
  location_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  location_name VARCHAR2(120) NOT NULL,
  address VARCHAR2(250) NOT NULL,
  user_id NUMBER NOT NULL,
  CONSTRAINT fk_saved_location_user FOREIGN KEY (user_id) REFERENCES users (user_id)
);

CREATE TABLE cab_maintenance (
  maintenance_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  service_date DATE NOT NULL,
  service_type VARCHAR2(120) NOT NULL,
  cost NUMBER(10,2) NOT NULL,
  cab_id NUMBER NOT NULL,
  technician VARCHAR2(120),
  notes VARCHAR2(500),
  status VARCHAR2(40) DEFAULT 'Scheduled' NOT NULL,
  CONSTRAINT fk_maintenance_cab FOREIGN KEY (cab_id) REFERENCES cabs (cab_id)
);

CREATE SEQUENCE bookings_seq START WITH 8 INCREMENT BY 1 NOCACHE;
